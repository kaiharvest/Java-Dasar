public class Method {
	public static void main(String[] args) {

		// method adalah block kode yang akan berjalan saat kita panggil
		// untuk membuat method di java, kita bisa menggunakan kata kunci void diikuti dengan nama method nya
		// method juga disebut dengan function

		sayHelloWorld();

	}

	static void sayHelloWorld() {
		System.out.println("Hello World 1");
		System.out.println("Hello World 2");
		System.out.println("Hello World 3");
	}
}
